int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	if (!(s1 && s2))
	{
		if (!s1 && !s2)
			return (0);
		else if (!s1)
			return (1);
		else
			return (-1);
	}
	while (*s1 && *s1 == *s2 && n > 0)
	{
		s1++;
		s2++;
		n--;
	}
	return (*s1 - *s2);
}
